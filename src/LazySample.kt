/*
* 属性委托：lazy 委托
* 作用：在属性值发生变化时，自动通知观察者
* 特性：懒加载,只在第一次调用时执行委托表达式
* 延迟执行：{ ... } 块中的代码只在第一次访问属性时执行
* 缓存机制：第一次执行后，结果被缓存，后续访问直接返回缓存值
* 只读属性：通常用于 val（只读属性），因为初始化后值不再改变
* */

val name: String by lazy {
    println("第一次用到才执行！")
    "Kotlin 委托"
}

fun main() {
    println("开始")
    println(name) // 第一次调用，才执行 lazy 里的代码
    println(name) // 第二次直接用缓存值
}